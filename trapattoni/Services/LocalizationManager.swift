// MARK: - Localized String Helper

struct L {
    static var manager: LocalizationManager { LocalizationManager.shared }

    static func get(_ key: String) -> String {
        manager.localized(key)
    }
}

// MARK: - View Extension for Localization Observation

extension View {
    /// Forces the view to observe LocalizationManager changes
    /// Use this modifier on views that need to re-render when language changes
    func observeLanguageChanges() -> some View {
        modifier(LanguageObservingModifier())
    }
}

private struct LanguageObservingModifier: ViewModifier {
    // This creates an observation relationship with LocalizationManager
    private var language: AppLanguage {
        LocalizationManager.shared.currentLanguage
    }

    func body(content: Content) -> some View {
        // Reading the language property establishes observation
        content.id(language.rawValue)
    }
}

// MARK: - String Extension for Localization

extension String {
    var localized: String {
        LocalizationManager.shared.localized(self)
    }

    func localized(with args: CVarArg...) -> String {
        let format = LocalizationManager.shared.localized(self)
        return String(format: format, arguments: args)
    }
}

// MARK: - Environment Key for Localization

private struct LocalizationManagerKey: EnvironmentKey {
    static let defaultValue = LocalizationManager.shared
}

extension EnvironmentValues {
    var localization: LocalizationManager {
        get { self[LocalizationManagerKey.self] }
        set { self[LocalizationManagerKey.self] = newValue }
    }
}
